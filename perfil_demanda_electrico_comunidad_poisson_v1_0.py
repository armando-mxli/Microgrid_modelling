@dataclass
class Appliance:
    name: str
    power_W: float
    mode: str  # "always_on", "fixed_schedule", "hour_slots", "events", "cycling"

    # Daily usage
    daily_usage_h: Optional[float] = None
    daily_usage_range_h: Optional[tuple] = None

    # Event duration
    event_duration_h: Optional[float] = None

    # Hour weights (24 values)
    hour_weights: List[float] = field(default_factory=lambda: [1.0] * 24)

    # Event start-hour weights (24 values)
    start_hour_weights: Optional[List[float]] = None

    # Fixed schedule (24 values of 0/1 or fractional use)
    fixed_hour_schedule: Optional[List[float]] = None

    # Weekday/weekend modifiers
    weekday_factor: float = 1.0
    weekend_factor: float = 1.0

    # Monthly modifiers
    month_factors: Dict[int, float] = field(default_factory=lambda: {m: 1.0 for m in range(1, 13)})

    # Limit active hours/day
    max_daily_hours: Optional[float] = None

    # Cycling loads (e.g., refrigerator)
    base_duty_cycle: Optional[float] = None


