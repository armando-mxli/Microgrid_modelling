# 4) HELPER FUNCTIONS
# -----------------------------
def normalize_weights(w):
    w = np.array(w, dtype=float)
    w[w < 0] = 0.0
    s = w.sum()
    if s <= 0:
        w = np.ones_like(w, dtype=float)
        s = w.sum()
    return w / s


def sample_unique_hours(k, weights, rng):
    """
    Sample k unique hours from 0..23 according to weighted probabilities.
    """
    weights = normalize_weights(weights)
    hours = np.arange(24)

    positive_mask = weights > 0
    positive_hours = hours[positive_mask]
    positive_weights = weights[positive_mask]

    if len(positive_hours) == 0 or k <= 0:
        return np.array([], dtype=int)

    k = min(int(k), len(positive_hours))
    positive_weights = positive_weights / positive_weights.sum()

    return np.sort(rng.choice(positive_hours, size=k, replace=False, p=positive_weights))


def sample_event_start_hours(n_events, weights, rng):
    """
    Sample event start hours with replacement.
    """
    weights = normalize_weights(weights)
    hours = np.arange(24)

    if n_events <= 0:
        return np.array([], dtype=int)

    return rng.choice(hours, size=int(n_events), replace=True, p=weights)


def add_event_block(day_profile_W, start_hour, duration_h, power_W):
    """
    Add an event block to the daily hourly profile.
    Supports fractional duration.
    """
    remaining = duration_h
    h = int(start_hour)

    while remaining > 1e-9 and h < 24:
        frac = min(1.0, remaining)
        day_profile_W[h] += power_W * frac
        remaining -= frac
        h += 1


def get_day_factor(is_weekend, appliance):
    return appliance.weekend_factor if is_weekend else appliance.weekday_factor


def get_month_factor(month, appliance):
    return appliance.month_factors.get(month, 1.0)


def simulate_one_household_profile(appliances, calendar_df, unique_days, rng):
    """
    Simulate one household over the full year.
    Returns an hourly power profile in W (length 8760).
    """
    profile_W = np.zeros(len(calendar_df), dtype=float)
    date_to_idx = calendar_df.groupby("date").indices

    for day in unique_days:
        day_idx = date_to_idx[day]
        day_slice = calendar_df.iloc[day_idx]

        month = int(day_slice["month"].iloc[0])
        is_weekend = bool(day_slice["is_weekend"].iloc[0])

        day_profile_W = np.zeros(24, dtype=float)

        for ap in appliances:
            day_factor = get_day_factor(is_weekend, ap)
            month_factor = get_month_factor(month, ap)

            # -----------------------------------
            # ALWAYS ON
            # -----------------------------------
            if ap.mode == "always_on":
                if ap.daily_usage_h is None:
                    raise ValueError(f"{ap.name}: daily_usage_h is required for mode='always_on'")
                duty = min(ap.daily_usage_h / 24.0, 1.0)
                day_profile_W += ap.power_W * duty * day_factor * month_factor

            # -----------------------------------
            # FIXED SCHEDULE
            # -----------------------------------
            elif ap.mode == "fixed_schedule":
                if ap.fixed_hour_schedule is None:
                    raise ValueError(f"{ap.name}: fixed_hour_schedule is required for mode='fixed_schedule'")

                schedule = np.array(ap.fixed_hour_schedule, dtype=float)
                if len(schedule) != 24:
                    raise ValueError(f"{ap.name}: fixed_hour_schedule must contain 24 values")

                multiplier = day_factor * month_factor
                day_profile_W += ap.power_W * schedule * multiplier

            # -----------------------------------
            # HOUR SLOTS
            # -----------------------------------
            elif ap.mode == "hour_slots":
                if ap.daily_usage_range_h is not None:
                    base_mean_h = rng.uniform(ap.daily_usage_range_h[0], ap.daily_usage_range_h[1])
                elif ap.daily_usage_h is not None:
                    base_mean_h = ap.daily_usage_h
                else:
                    raise ValueError(f"{ap.name}: daily_usage_h or daily_usage_range_h required for mode='hour_slots'")

                mean_h = base_mean_h * day_factor * month_factor

                if ap.max_daily_hours is not None:
                    mean_h = min(mean_h, ap.max_daily_hours)

                active_hours = rng.poisson(lam=max(mean_h, 0.0))
                active_hours = min(active_hours, 24)

                chosen_hours = sample_unique_hours(active_hours, ap.hour_weights, rng)
                day_profile_W[chosen_hours] += ap.power_W

            # -----------------------------------
            # EVENTS
            # -----------------------------------
            elif ap.mode == "events":
                if ap.daily_usage_h is None:
                    raise ValueError(f"{ap.name}: daily_usage_h is required for mode='events'")
                if ap.event_duration_h is None:
                    raise ValueError(f"{ap.name}: event_duration_h is required for mode='events'")

                mean_daily_usage_h = ap.daily_usage_h * day_factor * month_factor
                duration_h = ap.event_duration_h

                mean_events = max(mean_daily_usage_h / duration_h, 0.0)
                n_events = rng.poisson(lam=mean_events)

                start_weights = ap.start_hour_weights if ap.start_hour_weights is not None else ap.hour_weights
                start_hours = sample_event_start_hours(n_events, start_weights, rng)

                for sh in start_hours:
                    add_event_block(day_profile_W, int(sh), duration_h, ap.power_W)

            # -----------------------------------
            # CYCLING LOADS
            # -----------------------------------
            elif ap.mode == "cycling":
                if ap.base_duty_cycle is None:
                    raise ValueError(f"{ap.name}: base_duty_cycle is required for mode='cycling'")

                hour_weights = np.array(ap.hour_weights, dtype=float)
                hour_weights = hour_weights / np.mean(hour_weights)  # normalize around 1.0

                for h in range(24):
                    duty_h = ap.base_duty_cycle * hour_weights[h] * day_factor * month_factor

                    # Add small stochastic variation
                    duty_h *= rng.uniform(0.90, 1.10)

                    # Physical limits
                    duty_h = np.clip(duty_h, 0.05, 0.95)

                    day_profile_W[h] += ap.power_W * duty_h

            else:
                raise ValueError(f"Unknown appliance mode: {ap.mode}")

        profile_W[day_idx] = day_profile_W

    return profile_W


